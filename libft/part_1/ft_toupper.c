#include "../libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	else if (c < -1)
	{
		c = (unsigned char)c;
		return ((int)c);
	}
	else
		return (c);
}
